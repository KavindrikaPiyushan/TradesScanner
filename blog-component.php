// Function to render a single blog post
function renderBlogPost($post) {
    return '
    <div class="other-blog">
        <div class="categoryLabel">
                   Finance
            </div>
        <img src="' . $post['image'] . '" alt="" class="otherBlogImg">
        <div class="blogDescriptionCover otherBlogDescriptionCov">
            <div class="otherBlogHead">
                <div class="author">
                    <p>Posted: </p>
                    <h6>' . $post['posted'] . '</h6>
                </div>
                <div class="reacts">
                    <p>' . $post['reactions'] . '</p>
                    <img src="./images/Greenheart.png" alt="" class="heartIcon">
                </div>
            </div>
            <div class="otherBlogAuthorHead">
                <img src="' . $post['authorImage'] . '" alt="" class="authorImg">
                <div class="author">
                    <h5>' . $post['authorName'] . '</h5>
                </div>
            </div>
            <h3 class="otherBlogTitle">
                ' . $post['title'] . '
            </h3>
            <p class="otherblogDescription">
                ' . $post['description'] . '
            </p>
            <div class="blogFooter">
                <div class="comments">
                    <img src="./images/comment.png" alt="" class="commentIcon">
                    <p>' . $post['comments'] . ' Comments</p>
                </div>
                <div class="flag">
                    <img src="' . $post['flag'] . '" alt="" class="flagImg">
                    <p>' . $post['readTime'] . '</p>
                </div>
            </div>
        </div>
    </div>';
}

// Create the complete blog section
function renderBlogSection($posts) {
    $output = '<div class="other-blog-cover">';
    
    foreach ($posts as $post) {
        $output .= renderBlogPost($post);
    }
    
    $output .= '</div>';
    
    return $output;
}

